export function getResponse(query) {
  const q = query.toLowerCase();
  let matchedId = "bg-2-47"; // default

  for (const [keyword, verseId] of Object.entries(themeMap)) {
    if (q.includes(keyword)) {
      matchedId = verseId;
      break;
    }
  }

  const verse = verses.find(v => v.id === matchedId) || verses[0];
  const resp = responses[matchedId] || responses["bg-2-47"];

  return {
    id: `resp-${Date.now()}`,
    summary: resp.summary,
    verse,
    mindsetShift: resp.mindsetShift,
    closing: resp.closing,
  };
}

export function getDailyVerse() {
  const dayOfYear = Math.floor((Date.now() - new Date(new Date().getFullYear(), 0, 0)) / 86400000);
  return verses[dayOfYear % verses.length];
}
